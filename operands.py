a_number = 20
another_number = 4
multiplier = 5
power = 2

sum = a_number + another_number
print(a_number," + ",another_number," = ",sum,end="\n\n")

difference = a_number - another_number
print(a_number," - ",another_number," = ",difference,end="\n\n")

product = a_number * multiplier
print(a_number," * ",multiplier," = ",product,end="\n\n")

number_raised_to_the_power = a_number ** power
print(a_number," ** ",power," = ",number_raised_to_the_power,end="\n\n")

float_division = a_number / another_number
print(a_number," / ",another_number," = ",float_division,end="\n\n")

integer_division = a_number // another_number
print(a_number," // ",another_number," = ",integer_division,end="\n\n")

remainder = a_number % another_number
print(a_number," % ",another_number," = ",remainder)
